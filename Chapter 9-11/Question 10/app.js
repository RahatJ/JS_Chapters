var temp=prompt("Enter Temperature")
if(temp>40){
    document.write("<br>")
    document.write("It is too hot outside")
}

else if(temp>30){
    document.write("<br>")
    document.write("The Weather today is normal")
}

else if(temp>20){
    document.write("<br>")
    document.write("Today's weather is cool")
}

else {
    document.write("<br>")
    document.write("OMG! Today's weather is soo cool")
}